public class ArithmeticExpressions {
    public static void main(String[] args) {
      //arithmetic expressions = operands & operators
      // operands = values, variables, numbers, quantity
      // operators = + - * / %
        int friends = 15;
        friends = friends +1;
        friends = friends *2;
        friends = friends -1;
        friends = friends /3;
        friends = friends %2;
        System.out.println(friends );


    }
}
